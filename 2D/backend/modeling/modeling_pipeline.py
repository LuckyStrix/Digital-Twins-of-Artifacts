def _cuda_available() -> bool:
    """Check whether a CUDA-capable GPU is available."""
    try:
        import torch
        return torch.cuda.is_available()
    except ImportError:
        return False


def _postprocess_mask(mask: np.ndarray, dilate_px: int = 0) -> np.ndarray:
    """
    Clean up a raw model mask:
      - Binarise to hard 0/255 (eliminates soft-edge uncertainty at boundaries)
      - Optionally dilate to recover fine ink details near the fragment edge
    """
    _, mask = cv2.threshold(mask, 127, 255, cv2.THRESH_BINARY)
    if dilate_px > 0:
        kernel = cv2.getStructuringElement(cv2.MORPH_ELLIPSE, (dilate_px * 2 + 1,) * 2)
        mask   = cv2.dilate(mask, kernel)
    return mask


def _run_rembg(image_path: Path, model_name: str) -> np.ndarray:
    """
    Segment with rembg (ONNX-based, multiple lightweight models).

    Explicitly passes the provider list so CUDA is used when available -
    rembg otherwise silently falls back to CPU even when rembg[gpu] is installed.

    Returns uint8 grayscale mask: 255 = foreground, 0 = background.
    """
    try:
        from rembg import remove, new_session
        import onnxruntime as ort
    except ImportError:
        print("  ERROR: rembg is not installed.")
        print("    Install:  pip install rembg       (CPU only)")
        print("              pip install rembg[gpu]  (with CUDA)")
        sys.exit(1)

    available = ort.get_available_providers()
    if "CUDAExecutionProvider" in available:
        providers    = ["CUDAExecutionProvider", "CPUExecutionProvider"]
        device_label = "CUDA"
    else:
        providers    = ["CPUExecutionProvider"]
        device_label = "CPU (CUDAExecutionProvider not found in onnxruntime)"

    print(f"  Device  : {device_label}")
    print(f"  Loading rembg session with model '{model_name}'...")
    print("  (Model weights will download automatically on first run.)")

    session = new_session(model_name, providers=providers)

    print(f"  Running inference on '{image_path.name}'...")
    result = remove(Image.open(image_path).convert("RGB"), session=session)

    # Alpha channel: 255 = foreground (keep), 0 = background (discard)
    return np.array(result)[:, :, 3].astype(np.uint8)


def _run_sam2(image_path: Path) -> np.ndarray:
    """
    Segment with SAM2 using a center-point prompt.

    Assumes the scroll fragment occupies the central region of the frame.
    Adjust point_coords below if the fragment is off-centre.
    Model weights (~900 MB) are cached in ~/.cache/huggingface/ after first run.

    Returns uint8 grayscale mask: 255 = foreground, 0 = background.
    """
    try:
        import torch
        from sam2.sam2_image_predictor import SAM2ImagePredictor
    except ImportError:
        print("  ERROR: sam2 is not installed.")
        print("    Install:  pip install sam2")
        sys.exit(1)

    device = "cuda" if _cuda_available() else "cpu"
    print(f"  Device : {device}")
    print("  Loading SAM2 model (weights download on first run ~900 MB)...")

    predictor = SAM2ImagePredictor.from_pretrained(
        "facebook/sam2.1-hiera-large", device=device
    )

    image_bgr = cv2.imread(str(image_path))
    image_rgb = cv2.cvtColor(image_bgr, cv2.COLOR_BGR2RGB)
    h, w      = image_rgb.shape[:2]

    print(f"  Running SAM2 on '{image_path.name}' ({w}×{h})...")
    print("  Prompt: center point as foreground.")

    point_coords = np.array([[w // 2, h // 2]])
    point_labels = np.array([1])   # 1 = foreground

    with torch.inference_mode(), torch.autocast(device, dtype=torch.bfloat16):
        predictor.set_image(image_rgb)
        masks, scores, _ = predictor.predict(
            point_coords=point_coords,
            point_labels=point_labels,
            multimask_output=True,   # returns 3 candidate masks at different scales
        )

    best_mask = masks[np.argmax(scores)]
    print(f"  Best mask score: {np.max(scores):.3f}")
    return (best_mask * 255).astype(np.uint8)


