#!/usr/bin/env python3
"""
modeling_pipeline.py
=====================
Complete five-stage photometric stereo pipeline for 3D scroll modeling.

This is the "Modeling" stage of the integrated Papyrus app (see ../../run.py).
It reads the raw scroll photographs from the active scan folder (set by run.py
via PAPYRUS_SCROLL_DIR) and the calibration shots from backend/calibration/, and
writes texture maps to the render-ready output folder, ready for the Rendering
stage to turn into a textured 3D model.

  Stage 0 - Alpha Mask Generation
    Uses a pretrained ML segmentation model (rembg or SAM2) to isolate the
    scroll from the background, producing the binary AlphaMask used by all
    subsequent stages.

  Stage 1 - Lighting Calibration
    Corrects vignetting and per-light illumination falloff using photos of flat
    copy paper, producing corrected scroll images that behave as if lit by
    uniform, infinitely distant lights with a perfect lens.

  Stage 2 - Core Maps (Normal, Diffuse, Specular, Roughness)
    Derives the normal map from the calibrated cross-polarised images using
    a simple gradient approach.  Also computes the diffuse, specular, and
    roughness maps that the renderer needs.  The roughness map is derived
    from the specular map without inversion: papyrus fibres depolarise
    light strongly (high co-cross signal) -> high roughness (matte);
    ink absorbs strongly (low co-cross) -> floored at 0.6 to avoid a
    mirror-smooth appearance.

  Stage 3 - Height Map Generation
    Runs photometric stereo on the calibrated images to recover surface normals,
    then integrates them into height maps via weighted Frankot-Chellappa.
    Six methods are computed (cross-pol, co-pol, specular component, 8-light
    combined, per-RGB-channel, plus integration of the Stage 2 normal map).

  Stage 4 - Prep for Rendering
    Rotates all maps 90° counter-clockwise and pads spatial dimensions to the
    nearest power of two, as required by most real-time renderers.

─────────────────────────────────────────────────────────────────────────────
DIRECTORY LAYOUT - place your files here before running:
─────────────────────────────────────────────────────────────────────────────

  SCROLL_DIR             ← the scroll scans to process: allLight.tiff +
      allLight.tiff            the 8 directional TIFFs. Set by run.py via the
      nco.tiff  sco.tiff       PAPYRUS_SCROLL_DIR env var (a data/<timestamp>/
      eco.tiff  wco.tiff       folder); falls back to the path below when run
      ncross.tiff  scross.tiff   standalone.
      ecross.tiff  wcross.tiff

  ../calibration/        ← same filenames shot on flat copy paper (no scroll),
      nco.tiff  ncross.tiff      in backend/calibration/
      sco.tiff  scross.tiff
      eco.tiff  ecross.tiff
      wco.tiff  wcross.tiff
      allLight.tiff                                     (sanity check only)

  reference_maps/        ← next to this script (backend/modeling/); holds the
      AlphaMask.tiff         AlphaMask generated by Stage 0 (or placed manually)

─────────────────────────────────────────────────────────────────────────────
OUTPUTS WRITTEN BY THIS SCRIPT (into output/ next to this file):
─────────────────────────────────────────────────────────────────────────────

  output/
    1_calibrated/        ← Stage 1: illumination-corrected images
      nco_cal.tiff  ncross_cal.tiff  sco_cal.tiff  scross_cal.tiff
      eco_cal.tiff  ecross_cal.tiff  wco_cal.tiff  wcross_cal.tiff

    2_maps/              ← Stage 2: normal, diffuse, specular, and roughness maps
      NormalMap_Cal.tiff
      DiffuseMap_Cal.tiff
      SpecularMap_Cal.tiff
      SpecularMap_Cal_stretched.tiff
      RoughnessMap_Cal.tiff

    3_height_maps/       ← Stage 3: height maps from six photometric stereo methods
      normalmap_cal_weighted_frankot_u16.tiff
      cross_ps_theta36.98_weighted_frankot_u16.tiff
      co_ps_theta36.98_weighted_frankot_u16.tiff
      specular_component_theta36.98_weighted_frankot_u16.tiff
      combined_8light_theta36.98_weighted_frankot_u16.tiff   ← fed into Stage 4
      cross_ps_channelR/G/B_theta36.98_weighted_frankot_u16.tiff

    4_render_ready/      ← Stage 4: rotated + power-of-two padded, ready for renderer
      AlphaMask_render.tiff
      HeightMap_render.tiff
      DiffuseMap_render.tiff
      NormalMap_render.tiff
      SpecularMap_render.tiff
      RoughnessMap_render.tiff

  run.py's "Build 3D Model" step copies output/4_render_ready/*_render.tiff
  into ../rendering/public/textures/ for the Three.js viewer.

─────────────────────────────────────────────────────────────────────────────
Coordinate convention (pixel space):
  x = column direction (rightward)
  y = row direction (downward)
  z = toward camera (out of image plane)

  North = Left of image  ->  N light direction: (-cos θ,  0, sin θ)
  South = Right          ->  S:                 (+cos θ,  0, sin θ)
  East  = Top            ->  E:                 ( 0, -cos θ, sin θ)
  West  = Bottom         ->  W:                 ( 0, +cos θ, sin θ)
─────────────────────────────────────────────────────────────────────────────
"""

