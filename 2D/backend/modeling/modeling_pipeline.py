    if SKIP_MASK_GENERATION:
        print("\n" + "═" * 62)
        print("  STAGE 0 - ALPHA MASK  [skipped - using existing mask]")
        print("═" * 62)
        return
    print("\n")
    print("  STAGE 0 - ALPHA MASK GENERATION")
    print("\n")
    print(f"  Backend : {MASK_BACKEND}")
    if MASK_BACKEND == "rembg":
        print(f"  Model   : {MASK_MODEL}")
    print(f"  Input   : {MASK_SOURCE_IMG}")
    if MASK_DILATE_PX:
        print(f"  Dilate  : {MASK_DILATE_PX} px")
    print()

    MASKS_DIR.mkdir(parents=True, exist_ok=True)
    source = SCROLL_DIR / MASK_SOURCE_IMG

    if MASK_BACKEND == "rembg":
        raw_mask = _run_rembg(source, MASK_MODEL)
