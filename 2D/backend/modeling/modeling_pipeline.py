# ═══════════════════════════════════════════════════════════════════════════════
# STAGE 0 - ALPHA MASK GENERATION
# ═══════════════════════════════════════════════════════════════════════════════
#
# WHY ML SEGMENTATION:
#   The scroll fragment has irregular edges with thin, wispy papyrus fibres.
#   A simple brightness threshold misclassifies dark ink areas inside the
#   fragment as background, and bright background areas as foreground.  A
#   pretrained segmentation model handles these edge cases far more robustly.
#
# Runs on every full pipeline execution by default, regenerating
# reference_maps/AlphaMask.tiff (next to this script) from MASK_SOURCE_IMG via
# the configured ML backend (see MASK_BACKEND/MASK_MODEL above).
# Set to True to skip Stage 0 and reuse an existing AlphaMask.tiff instead.
